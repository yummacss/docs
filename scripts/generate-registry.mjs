#!/usr/bin/env node

import { existsSync, readdirSync, writeFileSync } from "node:fs";
import { basename, join } from "node:path";

const cwd = process.cwd();
const uiDir = join(cwd, "src/registry/ui");
const outFile = join(cwd, "src/registry/index.ts");

function getIds(dir) {
  if (!existsSync(dir)) return [];
  return readdirSync(dir)
    .filter((f) => f.endsWith(".tsx") || f.endsWith(".ts"))
    .map((f) => basename(f, f.endsWith(".tsx") ? ".tsx" : ".ts"));
}

const uiIds = getIds(uiDir);

const uiLines = uiIds
  .map((id) => `  "${id}": () => import("./ui/${id}"),`)
  .join("\n");

const output = `/**
 * AUTO-GENERATED — do NOT edit this file manually!
 * Run: node scripts/generate-registry.mjs
 */

// ---------------------------------------------------------------------------
// Yumma UI Registry
// ---------------------------------------------------------------------------

export const registry = {
${uiLines}
} as const;

export type RegistryId = keyof typeof registry;
export type RegistryImport = () => Promise<{ default: React.ComponentType<object> }>;

export function getRegistryImport(id: string): RegistryImport | null {
  return (registry as Record<string, RegistryImport>)[id] ?? null;
}
`;

writeFileSync(outFile, output, "utf-8");
console.log(`✓ Registry generated: ${uiIds.length} Yumma UI components.`);
